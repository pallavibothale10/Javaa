package Variable;

public class Sample2 {
	
	
	public static void main (String[] args)     //Main Method body 
	{
	//1. Variable Declaration
		
		String StudentFirstName;             //Pallavi
		String StudentLastName;           //Bothale
        int    StudentRollno;              //10
        float  StudentPercentage;            //75.60
        char   StudentGrade;                 //A

		//2. Variable Initialization
		
        StudentFirstName = "Pallavi";
        StudentLastName = "Bothale";
        StudentRollno = 10;
        StudentPercentage = 80.55f;
        StudentGrade = 'A';
		
		//3. Usages
		
		System.out.println("FirstName :" +StudentFirstName);         //Printing Statement
        System.out.println("LastName :" +StudentLastName);            //Printing Statement
        System.out.println("Rollno :" +StudentRollno);                //Printing Statement
        System.out.println ("Percentage :" +StudentPercentage)  ;     //Printing Statement
        System.out.println("Grade :" +StudentGrade);                  //Printing Statement
	}
}
