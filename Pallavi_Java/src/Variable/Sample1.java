package Variable;

public class Sample1 {

	public static void main (String[] args)     //Main Method body 
	{
	//1. Variable Declaration
		
		String FirstName;
		String LastName;

		
		//2. Variable Initialization
		//One variable can store only one Information at a time
		
		FirstName = "Pallavi";
		LastName = "Bothale";
		
		//3. Usages
		
		System.out.println(FirstName);             //Printing Statement
        System.out.println(LastName);             //Printing Statement

		
	}
	
	
}