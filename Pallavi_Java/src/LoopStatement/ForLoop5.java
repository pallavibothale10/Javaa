package LoopStatement;

public class ForLoop5 {
	public static void main(String[] args) {
		//WAP to print 1 to 20  numbers
	
		//output--> 1 3 5 7 9 11 ---20
		//Starting point-->1
		//Ending point--> 20
		//inc/dec--> i=i+2
		
		for(int i=1 ; i<=20 ; i=i+2 ) //i=i+1-->i++
		{
			System.out.println(i);
		}
	}

}
