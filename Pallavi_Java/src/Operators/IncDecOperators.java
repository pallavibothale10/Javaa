package Operators;

public class IncDecOperators {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		int c;
		
		c = a;     //Assign operator
		System.out.println(c);
		
		a++;                   //Increment by one
		System.out.println(a);        //Increment operator
		
		c=c+1;                //Increment by one
		System.out.println(c);
		
		c=c+2;
		System.out.println(c);
		
		c--;
		System.out.println(c);
		
		c=c-1;
		System.out.println(c);
		
		c=c-2;
		System.out.println(c);
		
	}
}
