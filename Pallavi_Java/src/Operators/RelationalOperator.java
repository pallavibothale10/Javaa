package Operators;

public class RelationalOperator {
	public static void main(String[] args) {
		
		int A = 30;
		int B = 50;
		System.out.println(A>B);      //30>50 false
		System.out.println(A>=B);        //30>=50 false
		System.out.println(A<B);        //30<50 true
		System.out.println(A<=B);        //30<=50 true
		System.out.println(A==B);          //30==50 false
		System.out.println(A!=B);      //30!=50 true

	}

}