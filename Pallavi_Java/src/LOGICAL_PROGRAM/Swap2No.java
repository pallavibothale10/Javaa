package LOGICAL_PROGRAM;

public class Swap2No {
	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		int c;
		System.out.println("Before swapping no: " +a +" " +b);
		
		c=a;//10
		a=b; //20
		b=c; //10
		System.out.println("After Swapping: " +a +" " +b);
		
	}

}
