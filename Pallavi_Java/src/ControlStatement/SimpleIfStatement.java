package ControlStatement;

public class SimpleIfStatement {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		
		/* Simple If Statement syntax--> 
		 * if(Condition){
		 * Statement 1  is executed when condition is TRUE
		 * }
		 */
		
		if(a+b>20) 
		{      //10+20 > 20
			System.out.println("a+b is greater than 20");
		}
		System.out.println("Program completed");
		
	}
}
